/* 
    OPERADORES DE COMPARAÇÃO
    ==  : Igualdade
    !=  : Diferença
    >   : Maior
    <   : Menor
    >=  : Maior ou igual
    <=  : Menor ou igual
    !   : Negação
    === : Igualdade entre tipos de variaveis/constante (valores)
    !== : Diferença entre tipos de variaveis/constante (valores)
*/

console.log( 7 !== '7' );
console.log( 'number' !== 'string' );


